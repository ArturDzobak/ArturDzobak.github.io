let n = prompt("Введіть число:");

if (isNaN(n)) {
    alert("Введіть коректне число, спробуйте ще раз.");
} else {
    n = parseFloat(n);

    while (n % 3 === 0) {
        n /= 3;
    }

    if (n === 1) {
        alert("Введене число є ступенем числа 3.");
    } else {
        alert("Введене число не є ступенем числа 3.");
    }
}
